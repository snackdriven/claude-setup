#!/bin/bash
# cron-alert.sh — shared failure surface for launchd/cron jobs in personal/
#
# Usage:
#   cron-alert.sh JOB_NAME EXIT_CODE [ERR_LOG_PATH]
#
# Does three things on call:
#   1. Appends one line to ~/.claude/logs/cron-failures.log
#   2. Writes/updates dailies/<today>/.cron-failures.md (daily-prep reads this)
#   3. Captures last 10 lines from err log (if provided / inferable) for context
#
# Convention: if ERR_LOG_PATH is omitted, infer ~/.local/bin/.<JOB_NAME>.err

set -u

JOB="${1:-unknown}"
RC="${2:-?}"
ERR_LOG="${3:-$HOME/.local/bin/.${JOB}.err}"

TS=$(date '+%Y-%m-%d %H:%M:%S %Z')
TODAY=$(date '+%Y-%m-%d')
DAILY_DIR="$HOME/Desktop/personal/dailies/$TODAY"
MARKER="$DAILY_DIR/.cron-failures.md"
AGG_LOG="$HOME/.claude/logs/cron-failures.log"

mkdir -p "$DAILY_DIR" "$(dirname "$AGG_LOG")"

# Capture last 10 lines of err log for context
EXCERPT=""
if [ -n "$ERR_LOG" ] && [ -f "$ERR_LOG" ]; then
  EXCERPT=$(tail -n 10 "$ERR_LOG" 2>/dev/null || true)
fi

# 1. Append to aggregated log
{
  echo "[$TS] FAIL job=$JOB rc=$RC err_log=$ERR_LOG"
  if [ -n "$EXCERPT" ]; then
    echo "$EXCERPT" | sed 's/^/    /'
  fi
} >> "$AGG_LOG"

# 2. Write/append daily marker (daily-prep.ts reads this)
if [ ! -f "$MARKER" ]; then
  cat > "$MARKER" <<EOF
# Cron failures — $TODAY

Auto-generated by \`~/.local/bin/cron-alert.sh\`. Each entry is a non-zero exit from a scheduled job. Investigate, then move/delete this file once handled.

EOF
fi

{
  echo ""
  echo "## $JOB (rc=$RC, $TS)"
  echo ""
  echo "Err log: \`$ERR_LOG\`"
  if [ -n "$EXCERPT" ]; then
    echo ""
    echo '```'
    echo "$EXCERPT"
    echo '```'
  fi
} >> "$MARKER"

# 3. macOS banner — push the failure to her in real time. Best-effort, silent
#    fallback if osascript is unavailable or DND is on.
if command -v osascript >/dev/null 2>&1; then
  osascript -e "display notification \"rc=$RC — see $ERR_LOG\" with title \"⚠ launchd: $JOB failed\"" 2>/dev/null || true
fi

# 4. Drop a marker file the next Claude session can surface on SessionStart.
#    Single-line JSON so any hook can parse it without jq.
printf '{"job":"%s","rc":%s,"ts":"%s","err_log":"%s"}\n' \
  "$JOB" "$RC" "$TS" "$ERR_LOG" > "$HOME/.claude/.last-cron-failure.json"

exit 0
